namespace EndpointMapper;

/// <summary>
/// The supported HTTP Methods for mapping an endpoint
/// </summary>
public static class HttpMapMethod
{
    /// <summary>
    /// CONNECT HTTP Method
    /// </summary>
    public const string Connect = "CONNECT";
    /// <summary>
    /// DELETE HTTP Method
    /// </summary>
    public const string Delete = "DELETE";
    /// <summary>
    /// GET HTTP Method
    /// </summary>
    public const string Get = "GET";
    /// <summary>
    /// HEAD HTTP Method
    /// </summary>
    public const string Head = "HEAD";
    /// <summary>
    /// OPTIONS HTTP Method
    /// </summary>
    public const string Options = "OPTIONS";
    /// <summary>
    /// PATCH HTTP Method
    /// </summary>
    public const string Patch = "PATCH";
    /// <summary>
    /// POST HTTP Method
    /// </summary>
    public const string Post = "POST";
    /// <summary>
    /// PUT HTTP Method
    /// </summary>
    public const string Put = "PUT";
    /// <summary>
    /// TRACE HTTP Method
    /// </summary>
    public const string Trace = "TRACE";
}